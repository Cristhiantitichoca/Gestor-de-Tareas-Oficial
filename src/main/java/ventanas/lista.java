   /**
     * Carga las tareas desde un archivo y las muestra en la tabla.
     * También actualiza los filtros de nombre y usuario basados en las tareas cargadas.
     * 
     * @param archivo Ruta del archivo desde el que se cargarán las tareas.
     */
public void cargarTareasEnTabla(String archivo) {
        TareaManager tareaManager = new TareaManager();
        tareas = tareaManager.leerTareasDesdeArchivo(archivo);

        // Limpiar los JComboBox antes de llenarlos
        filtro_nombre.removeAllItems();
        filtro_usuario.removeAllItems();

        filtro_nombre.addItem("Todos"); // Añadir la opción "Todos" para nombre
        filtro_usuario.addItem("Todos"); // Añadir la opción "Todos" para usuario

        DefaultTableModel model = (DefaultTableModel) tablaTareas.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de cargar

        for (Tarea tarea : tareas) {
            model.addRow(new Object[]{
                tarea.getUsuario(),
                tarea.getNombre(),
                tarea.getDescripcion(),
                tarea.getPrioridad(),
                tarea.getEstado().trim(),
            });

            // Añadir el nombre de la tarea al filtro (sin duplicados)
            if (!yaExisteEnComboBox(filtro_nombre, tarea.getNombre())) {
                filtro_nombre.addItem(tarea.getNombre());
            }

            // Añadir el usuario al filtro (sin duplicados)
            if (!yaExisteEnComboBox(filtro_usuario, tarea.getUsuario())) {
                filtro_usuario.addItem(tarea.getUsuario());
            }
        
        }

        // Establecer el renderizador y editor de celda personalizado en la columna "ACCIONES"
        tablaTareas.getColumnModel().getColumn(5).setCellRenderer(new AccionRendererEditor());
        tablaTareas.getColumnModel().getColumn(5).setCellEditor(new AccionRendererEditor());
        tablaTareas.getColumnModel().getColumn(4).setCellRenderer(new EstadoCellRenderer());
// Fuerza el repintado de la tabla
        tablaTareas.repaint();

    }
/**
     * Verifica si un ítem ya existe en un JComboBox.
     * 
     * @param comboBox El JComboBox a verificar.
     * @param item El ítem que se desea verificar.
     * @return true si el ítem ya existe, false si no.
     */
private boolean yaExisteEnComboBox(javax.swing.JComboBox<String> comboBox, String item) {
    for (int i = 0; i < comboBox.getItemCount(); i++) {
        if (comboBox.getItemAt(i).equals(item)) {
            return true;
        }
    }
    return false;
}


